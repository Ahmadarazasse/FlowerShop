    {


        if ($request->input('location') == null) {
            return redirect()->back()->with(['error' => 'You need to fill all inputs']);
        }

        order::create([...$request->all(), 'items_id' => $request->input('id')]);

        return redirect()->back()->with(['successmessage' => 'You order has been made, wait we will inform you']);
